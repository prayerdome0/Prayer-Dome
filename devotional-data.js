// Get next update time in milliseconds
export function getNextUpdateTime() {
    const now = new Date();
    const hour = now.getHours();
    let nextUpdateHour;
    
    if (hour < 12) nextUpdateHour = 12;
    else if (hour < 17) nextUpdateHour = 17;
    else nextUpdateHour = 5; // Next day 5 AM
    
    let nextUpdate = new Date(now);
    nextUpdate.setHours(nextUpdateHour, 0, 0, 0);
    
    if (nextUpdate <= now) {
        nextUpdate.setDate(nextUpdate.getDate() + 1);
    }
    
    return nextUpdate - now;
}

// Format countdown time
export function formatCountdown(ms) {
    const hours = Math.floor(ms / 3600000);
    const minutes = Math.floor((ms % 3600000) / 60000);
    const seconds = Math.floor((ms % 60000) / 1000);
    return `${hours}h ${minutes}m ${seconds}s`;
}